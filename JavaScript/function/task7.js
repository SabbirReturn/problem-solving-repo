function checkName(name){
    let goodLastChars = ['a','y','i','e','o','u','w']
    if(typeof name !== 'string'){
        return `Invalid`
    }
    name = name.toLowerCase()
    let lastChar = name[name.length-1]
    if( goodLastChars.includes(lastChar)){
        return ` Good Name`
    }
    else{
        return `Bad Name`
    }
    
    
}

